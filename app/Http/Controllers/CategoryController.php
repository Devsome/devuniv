    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //categoryCode', 'categoryName', 'categoryParent', 'categoryIcon', 'categoryImage'
        $category = new Category;
        $category->categoryCode = $request->categoryCode;
        $category->categoryName = $request->categoryName;
        $category->categoryParent = $request->categoryParent;
        $category->categoryIcon = $request->categoryIcon;
        $category->categoryImage = $request->categoryImage;
        $category->save();
        Alert::success('Categor has been Created!');
        return redirect('/admin/category');
    }

